func printDecoderSub(prefix string, depth int32, spacer string) {
	count := 0

	var lastKey string
	var lastInfo emojiInfo

	for r, info := range revEmojis {
		rs := string(r)
		if strings.HasPrefix(rs, prefix) {
			lastKey = rs
			lastInfo = info
			count++
		}
	}

	if count == 1 {
		
		encinfo := uint64(0xff & lastInfo.version) << 24 | uint64(0xff & lastInfo.padding) << 16  | uint64(0xffff & lastInfo.ordinal);

		if(lastInfo.padding != padNone) {
			encinfo |= (0x80 << 56);
		}

		if lastKey == prefix {	
			fmt.Printf("%sreturn 0x%016x;\n", spacer, encinfo)
		} else if len(prefix)+1 == len(lastKey) {
			fmt.Printf("%sif(ecoji_getc(infp) == 0x%x){\n", spacer, ([]byte(lastKey))[depth])
			fmt.Printf("%s  return 0x%016x;\n", spacer, encinfo)
			fmt.Printf("%s}else{return -1;}\n", spacer)
		} else {
			panic("unhandled case")
		}

	} else if count > 1 {
		seen := map[byte]bool{}

		fmt.Printf("%sswitch(ecoji_getc(infp)){\n", spacer)
		for r, _ := range revEmojis {
			rs := string(r)
			if strings.HasPrefix(rs, prefix) {
				c := rs[depth]

				if !seen[c] {
					fmt.Printf("%s  case 0x%x:\n", spacer, c)

					printDecoderSub(prefix+string([]byte{c}), depth+1, spacer+"    ")

					//fmt.Printf("%s    break;\n", spacer)
					seen[c] = true
				}
			}
		}
		fmt.Printf("%s  default:\n", spacer)
		fmt.Printf("%s    return -1;\n", spacer)

		fmt.Printf("%s}\n", spacer)

	} else {
		panic("nothing seen for prefix " + hex.EncodeToString([]byte(prefix)))
	}

}

