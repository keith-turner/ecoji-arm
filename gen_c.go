func toHex(c rune) string {

	rlen := utf8.RuneLen(c)
	switch rlen {
	case 3:
		return "0x0300000000" + hex.EncodeToString([]byte(string(c)))
	case 4:
		return "0x04000000" + hex.EncodeToString([]byte(string(c)))
	default:
		panic("unexpected emoji utf8 len")
	}

}

